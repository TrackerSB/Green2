package bayern.steinbrecher.gruen2.login;

/**
 * Represents constants for description of login information such as ssh
 * password or ssh username.
 *
 * @author Stefan Huber
 */
public enum LoginKey {

    /**
     * SSH username.
     */
    SSH_USERNAME,
    /**
     * SSH password.
     */
    SSH_PASSWORD,
    /**
     * Database username.
     */
    DATABASE_USERNAME,
    /**
     * Database password.
     */
    DATABASE_PASSWORD;
}
